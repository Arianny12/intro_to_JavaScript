function homework(num){
  if (num <= 0){
    return "Yayyyyy I have no homeworks🥳"
  }
  else if (num == 1){
    return "I only have one homework🤨"
  }
  else if (num == 2){
    return "I can do this! is only two🙁"
  }
  else if (num == 3){
    return "I'm so tired😔"
  }
  else{
    return "THAT IS A LOT OF HOMEWORKS😤"
  }
}

